                   <td colspan="6" class="body_text"><h1><strong>An Overview of the Life and Work of Liane Collot d&rsquo;Herbois</strong></h1>
                   &copy;Pamela Whitman 2007
                   <p><img src="images/images/liane56.jpg" alt="Liane Collot h'Herbois" width="205" height="220" class="image_frame_left">Liane Collot d&rsquo;Herbois (1907-1999), born near Tintagel, Cornwall, England, of Scottish and French parentage, encountered the spiritual scientific work of Rudolf Steiner as a young professionally trained artist. She experienced Anthroposophy as the bridge between the spiritual world and practical life. She took up Steiner&rsquo;s work on color, emphasizing light and darkness, and made it visible and workable. She developed it by actually applying the creative forces of light and darkness in painting, both artistically and therapeutically. The result is the dynamic meeting of light and darkness creating <em>movement,</em> which manifests as visible color in a <em>transparent color space</em>. Collot d&rsquo;Herbois was the first to characterize these movements of color and create out of light and darkness a color space that is transparent, breaking through the flat,  solid boundaries of colored forms and planes, making it possible to see into and through the color space of the picture. </p>
                     <p>The field of Anthroposophical medicine was developed through Rudolf Steiner&rsquo;s close collaboration with the Dutch physician Dr. Ita Wegman (1876-1943). Their co-authored book, <em>Extending Practical Medicine </em>(formerly <em>Fundamentals of Therapy</em>)<em>, </em>is an extension of the art of healing through spiritual knowledge, providing a more complete picture of the human being, and thereby, the possibility for truly effective and innovative therapeutic approaches. Elisa M&eacute;trailler, who wrote the Foreword to <em>Light, Darkness and Color in Painting Therapy</em>, noted how Collot d&rsquo;Herbois&rsquo; early work with people of all ages showed her tremendous talent for seeing how constitution, temperament and illness can be revealed in one&rsquo;s paintings. Collot d&rsquo;Herbois worked closely with Ita Wegman and other physicians, developing her insights into a Painting Therapy based on the foundation of Anthroposophy.</p>
                     <p><img src="images/images/lcd1.jpg" alt="Painting by Liane Collot d'Herbois" width="194" height="245" class="image_frame_right">Collot d&rsquo;Herbois also pursued her own artistic work, though she considered herself more a &ldquo;painter&rdquo; than an &ldquo;artist&rdquo; in the more popular sense. She sought to create &ldquo;healing paintings&rdquo; through portraying windows to the spiritual world. This task requires work on oneself and understanding of the true Beings of Color, arising out of the meeting of Light and Darkness. In her book <em>Colour, Part One, </em>Collot d&rsquo;Herbois explains that through the use of the resulting laws of Light, Color and Darkness, one can move beyond subjectivity into a world of greater objective significance. Painting approached in this way can have a therapeutic and freeing effect on the viewer.</p>
                     <p>With the help of the Dutch painter and sculptor Francine van Davelaar (died 1984), she traveled through Europe and America, teaching and painting. She eventually settled in Holland, where she founded the &ldquo;Magenta Group&rdquo; for the painters who had gathered around her, sharing her discourses on the origins of color between light and darkness. This painting group provided the focus for her more artistic contributions, which resulted in her books <em>Colour, Part One </em>and <em>Colour, Part Two.</em></p>
                     <p><img src="images/images/lcd2.jpg" alt="Painting by Liane Collot d'Herbois" width="207" height="258" class="image_frame_left">In November 1978, Collot d&rsquo;Herbois was asked by the Dutch physician Dr. Paolo Walburgh Schmidt and painting therapist Josine Hutchison, to present her work on the use of color in therapeutic painting. She shared her knowledge and experience of color in relation to light and darkness in Painting Therapy to a core group of therapists and doctors. This endeavor then expanded to include others interested in really taking up her theoretical and practical work. The collaboration eventually resulted in the publication of her book <em>Light, Darkness and Colour in Painting Therapy </em>(1993, 2000).</p>
                     <p><img src="images/images/liane1.jpg" alt="Liane Collor d'herbois" width="205" height="270" class="image_frame_right">Two members of this group, the Dutch physician Dr. Paul Hutchison and painting therapist Josine Hutchison, were asked by Collot d&rsquo;Herbois herself, as well as therapists from Holland and abroad, to create a training for therapists. Courses began in 1986 and developed into a school in 1988 to offer a four-year training in Painting Therapy. <a href="education.php#schools">The Emerald Foundation</a>, in The Hague, <a href="education.php#schools">Holland</a>, founded in November 1988, was the first school recognized by the Medical Section of the School of Spiritual Science in Dornach, Switzerland, for offering an approved training in the Painting Therapy approach of Collot d&rsquo;Herbois. Now other schools have been established in <a href="education.php#schools">Paris</a>, <a href="education.php#schools">Florence</a>, and <a href="education.php#schools">Australia</a> and foundation work is taking place  in <a href="education.php#schools">North America</a>. Individuals trained in this work offer a variety of independent classes and workshops, especially in the artistic aspects. Painting therapists work individually with clients with a wide range of conditions, including physical, in consultation with their doctor when possible.</p>
                     <p>Collot d&rsquo;Herbois made extraordinary contributions toward the understanding of Light, Color and Darkness, showing their direct connection to the human being. Her therapeutic insights and transformative paintings have inspired doctors, therapists, artists, and those seeking a connection with spirit all over the world.</p>
